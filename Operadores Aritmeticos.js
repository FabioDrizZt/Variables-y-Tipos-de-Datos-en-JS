let incrementadora = 1
// pre incremento 
console.log(++incrementadora) // 2
// post incremento 
console.log(incrementadora++) // muestra 2 pero vale 3
console.log(incrementadora) // muestra 3

let decrementadora = 1
// pre decremento 
console.log(--decrementadora) // 0
// post decremento 
console.log(decrementadora--) // muestra 0 pero vale -1
console.log(decrementadora) // muestra -1

decrementadora = decrementadora-1